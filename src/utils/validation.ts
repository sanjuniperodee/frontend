import { z } from 'zod';

// Validation schemas
export const markerSchema = z.object({
  name: z.string()
    .min(3, 'Название должно содержать минимум 3 символа')
    .max(100, 'Название не должно превышать 100 символов'),
  description: z.string()
    .min(10, 'Описание должно содержать минимум 10 символов')
    .max(1000, 'Описание не должно превышать 1000 символов'),
  category: z.string().min(1, 'Выберите категорию'),
  latitude: z.number()
    .min(-90, 'Широта должна быть от -90 до 90')
    .max(90, 'Широта должна быть от -90 до 90'),
  longitude: z.number()
    .min(-180, 'Долгота должна быть от -180 до 180')
    .max(180, 'Долгота должна быть от -180 до 180'),
});

export const userSchema = z.object({
  username: z.string()
    .min(3, 'Имя пользователя должно содержать минимум 3 символа')
    .max(30, 'Имя пользователя не должно превышать 30 символов')
    .regex(/^[a-zA-Z0-9_-]+$/, 'Имя пользователя может содержать только буквы, цифры, _ и -'),
  password: z.string()
    .min(8, 'Пароль должен содержать минимум 8 символов')
    .regex(/^(?=.*[a-z])(?=.*[A-Z])(?=.*\d)/, 'Пароль должен содержать заглавные и строчные буквы, и цифры'),
});

export const commentSchema = z.object({
  content: z.string()
    .min(1, 'Комментарий не может быть пустым')
    .max(500, 'Комментарий не должен превышать 500 символов'),
});

export const categorySchema = z.object({
  name: z.string()
    .min(2, 'Название должно содержать минимум 2 символа')
    .max(50, 'Название не должно превышать 50 символов'),
  icon: z.string().min(1, 'Выберите иконку'),
  description: z.string().max(200, 'Описание не должно превышать 200 символов').optional(),
});